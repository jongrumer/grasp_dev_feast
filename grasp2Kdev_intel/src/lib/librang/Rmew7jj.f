      SUBROUTINE RMEW7JJ(J1,J2,K1,K2,COEF)
      IMPLICIT DOUBLEPRECISION (A-H,O-Z)
      COMMON/RIBOJJ/IMPTJJ(63),IMGTJJ(63),IMPNJJ(63),IMGNJJ(63)
      DIMENSION I00(14),I10(14),I01(14)
      DATA I00/32,48,128,80,96,128,20,60,20,108,36,44,156,68/
	DATA I10/6,9,120,15,18,24,2*30,0,54,2*0,78,0/
	DATA I01/10,35,168,165,286,680,0,30,10,180,60,110,546,408/
      COEF=0.00
      IF(IMPTJJ(J1) .NE. IMPTJJ(J2)) RETURN
      IF(J1.LT.12 .OR. J1.GT.25) RETURN
      IF(K1.EQ.0 .AND. K2.EQ.0) THEN
        IF(J1.NE.J2) RETURN
	  COEF=-DSQRT(DBLE(I00(J1-11)))
      ELSEIF(K1.EQ.1 .AND. K2.EQ.0) THEN
        IF(J1.NE.J2) RETURN
	  COEF=-DSQRT(DBLE(I10(J1-11)))
      ELSEIF(K1.EQ.0 .AND. K2.EQ.1) THEN
        IF(J1.NE.J2) RETURN
	  COEF=-DSQRT(DBLE(I01(J1-11))/DBLE(7))
	ELSE
        CALL RMEW7BJJ(J1,J2,K1,K2,COEF)
	ENDIF
      RETURN
      END
